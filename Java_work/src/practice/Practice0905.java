		// 올림 
		double i = Math.ceil(3.14);
		System.out.println(i); // 4.0
		
		// 내림 
		double a = Math.floor(10.5);
		System.out.println(a); // 10.0
		
		// 반올림 (소수점 둘째자리까지 )
		
		double b = Math.round((3.1415926585)*100)*0.01;
			System.out.println(b);
		
		//제곱
		int  d = (int) Math.pow(2, 8);
		System.out.println(d);
		
		//최대값
		int max = Math.max(10,25);
		System.out.println(max);
		
		//최소값
		int min = Math.min(10,25);
		System.out.println(min);
		
		System.out.println("------------------");
		

				
				
				
				
				
				
				
				
				
				
				
				
				
				
				
				
				
				
				
				
				
				
